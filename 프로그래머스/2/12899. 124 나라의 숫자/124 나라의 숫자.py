def solution(n):
    answer = ''
    while n > 0:
        r = n % 3 # n을 3으로 나눈 나머지
        if r == 0:
            answer += '4'
            n = n // 3 - 1
        else:
            answer += str(r)
            n = n // 3

    return answer[::-1]


# 124 나라의 숫자는 3진법과 유사하지만, 나머지가 0일 때의 처리가 다르다
# 10진법 숫자를 3으로 나눈 나머지에 따라 124 나라 숫자가 결정되는데:
# 나머지 1 → '1'
# 나머지 2 → '2'
# 나머지 0 → '4' (이 경우 몫을 1 줄여야 함)